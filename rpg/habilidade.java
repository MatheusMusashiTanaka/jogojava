// Classe Habilidade que possui nome e efeito

public class habilidade {
    public String nome;
    public String efeito;
    public int numero;
    public String tipo;

    public habilidade(String nome, String efeito, int numero, String tipo) {
        this.nome = nome;
        this.efeito = efeito;
        this.numero = numero;
        this.tipo = tipo;
    }
}
